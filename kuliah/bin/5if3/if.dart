void main() {
  int nilaiMahasiswa = 90;
  if (nilaiMahasiswa >= 90) {
    print("A");
  } else if (nilaiMahasiswa >= 80 && nilaiMahasiswa < 90) {
    print("AB");
  } else if (nilaiMahasiswa >= 70 && nilaiMahasiswa < 80) {
    print("B");
  } else if (nilaiMahasiswa >= 60 && nilaiMahasiswa < 70) {
    print("C");
  } else if (nilaiMahasiswa >= 50 && nilaiMahasiswa < 60) {
    print("D");
  } else {
    print("E");
  }
}
